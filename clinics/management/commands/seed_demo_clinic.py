class Command(BaseCommand):
    help = "Создаёт демо-стоматологию «Жемчуг Дент» с реалистичными услугами и FAQ"

    def add_arguments(self, parser):
        parser.add_argument(
            "--force",
            action="store_true",
            help="Пересоздать, даже если клиника уже существует",
        )
        parser.add_argument(
            "--phone",
            default=_PHONE,
            help=f"WhatsApp-номер (E.164 без +). По умолч.: {_PHONE}",
        )

    def handle(self, *args, **options):
        phone = options["phone"]
        force = options["force"]

        existing = Clinic.objects.filter(whatsapp_number=phone).first()
        if existing and not force:
            self.stdout.write(
                self.style.WARNING(
                    f"Клиника с номером {phone} уже существует (id={existing.pk}, "
                    f"name={existing.name!r}). Используйте --force для пересоздания."
                )
            )
            return

        if existing and force:
            # clinic.clinic — PROTECT: сначала удаляем зависимые данные (заявки и
            # переписки; сообщения уйдут каскадом за диалогами), потом саму клинику.
            existing.bookings.all().delete()
            existing.conversations.all().delete()
            existing.delete()
            self.stdout.write(f"  Старая запись (id={existing.pk}) удалена.")

        clinic = Clinic.objects.create(
            name="Жемчуг Дент",
            whatsapp_number=phone,
            address="г. Алматы, ул. Назарбаева, 45, офис 102 (ТЦ Меломан, 2 этаж)",
            services_json=_SERVICES,
            working_hours=_HOURS,
            faq=_FAQ,
            tone=_TONE,
            is_active=True,
        )

        self.stdout.write(self.style.SUCCESS(f"\n✓ Демо-клиника создана (id={clinic.pk})"))
        self.stdout.write(f"  Название:  {clinic.name}")
        self.stdout.write(f"  WhatsApp:  +{clinic.whatsapp_number}")
        self.stdout.write(f"  Адрес:     {clinic.address}")
        self.stdout.write(f"  Услуги:    {len(_SERVICES)} позиций")
        self.stdout.write(f"  FAQ:       {len(_FAQ)} вопросов")
        self.stdout.write(f"  Часы:      {', '.join(f'{k} {v}' for k, v in _HOURS.items())}")
        self.stdout.write("")
        self.stdout.write(
            "  Для теста бота выставьте в .env:\n"
            f"    WHATSAPP_PROVIDER=mock   (или evolution)\n"
            f"    AI_PROVIDER=groq         (или mock)\n"
            f"  Webhook: POST /webhook/whatsapp/\n"
        )
